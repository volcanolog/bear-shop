/* =====================================================================
 * sw.js — Service Worker приложения «Заметки»
 * Практическое занятие №13. Service Worker.
 * =====================================================================
 *
 * Что такое Service Worker (SW):
 *   - JS-скрипт, который браузер запускает в ОТДЕЛЬНОМ потоке,
 *     отдельно от страницы. Своего DOM у него нет.
 *   - Работает на основе событий (install, activate, fetch, message,
 *     push, sync).
 *   - Способен перехватывать сетевые запросы со страниц своей зоны
 *     видимости (scope) и отвечать на них из кэша → офлайн-режим.
 *   - Может «спать» и «просыпаться» по событиям. Никаких глобальных
 *     долгоживущих переменных в нём держать нельзя — между запусками
 *     состояние не сохраняется.
 *
 * Жизненный цикл SW (что нужно знать на экзамене):
 *   1. Регистрация в app.js: navigator.serviceWorker.register('./sw.js').
 *   2. install   — браузер скачал sw.js, вызывает событие. Здесь мы
 *                  кладём статические ресурсы в Cache Storage.
 *   3. waiting   — установленная новая версия ждёт, пока закроются все
 *                  вкладки со старой. skipWaiting() позволяет миновать
 *                  это ожидание.
 *   4. activate  — новый SW активен. Здесь чистим устаревшие кэши.
 *   5. controlling — SW начинает обслуживать страницы своей зоны.
 *                    clients.claim() даёт текущему SW взять под контроль
 *                    уже открытые вкладки без перезагрузки.
 *
 * Внутри SW:
 *   - self  — это ServiceWorkerGlobalScope (аналог window для страницы).
 *   - event.waitUntil(promise) — «не завершай событие, пока не выполнится
 *     промис». Без него браузер может прервать SW в середине работы.
 *   - event.respondWith(response) — внутри fetch-обработчика говорит
 *     браузеру: «вот ответ на запрос, не ходи в сеть сам».
 * ===================================================================== */


/* ---------- 1. Версионирование кэша ---------- */
//
// CACHE_NAME — это уникальное имя «коробки», в которой будут лежать
// закэшированные файлы. Когда меняем содержимое сайта, увеличиваем
// версию (v1 → v2). На событии 'activate' старые версии удаляются,
// а новый SW начинает раздавать свежие файлы.
//
// Это самый простой способ инвалидации кэша: сменили что-то в HTML/JS —
// подняли номер версии в этой строчке, и пользователь получит обновление.

const CACHE_VERSION = 'v1';
const CACHE_NAME = `notes-cache-${CACHE_VERSION}`;


/* ---------- 2. Список файлов для предварительного кэша ---------- */
//
// Это те файлы, без которых приложение в офлайне работать не сможет.
// Их называют «application shell» — оболочка приложения.
//
// '/' — корень: важно положить и его, и '/index.html', чтобы запрос
//       к домену без слэша на конце тоже отдавался из кэша.
//
// CDN-стили chota НЕ кладём сюда: для cross-origin ресурсов нужно
// аккуратно работать с режимом запроса (mode: 'no-cors' / 'cors'),
// и поведение зависит от заголовков CDN. Для учебной демонстрации
// проще ограничиться своими файлами.

const ASSETS = [
    './',
    './index.html',
    './app.js',
];


/* ---------- 3. Событие 'install' — кладём ресурсы в кэш ---------- */
//
// Срабатывает один раз для каждой версии SW. Если упадёт здесь —
// SW не установится и не активируется.
//
// Что делаем:
//   1) caches.open(CACHE_NAME) → создаём (или открываем) кэш.
//   2) cache.addAll(ASSETS) → скачиваем и сохраняем все ресурсы.
//      addAll атомарен: если хотя бы один файл не загрузится —
//      ВСЕ остальные тоже не сохранятся, install провалится.
//      Это защита от «полузакэшированного» состояния.
//   3) self.skipWaiting() → не ждём закрытия старых вкладок,
//      сразу переходим в фазу activate. Подходит, когда новая
//      версия совместима со старой страницей.

self.addEventListener('install', (event) => {
    console.log('[sw] install:', CACHE_NAME);

    event.waitUntil(
        caches.open(CACHE_NAME)
            .then((cache) => {
                console.log('[sw] кэшируем оболочку приложения:', ASSETS);
                return cache.addAll(ASSETS);
            })
            .then(() => self.skipWaiting())
    );
});


/* ---------- 4. Событие 'activate' — чистим старые кэши ---------- */
//
// Срабатывает, когда новая версия SW становится активной.
// Хороший момент, чтобы пройтись по всем кэшам и удалить те,
// чьи имена не совпадают с текущим CACHE_NAME (это и есть кэши
// от старых версий приложения).
//
// self.clients.claim() — берём контроль над уже открытыми вкладками
// сразу, не дожидаясь их перезагрузки. Без этого первая загрузка
// после регистрации SW идёт ещё «без него», и кэш не используется.

self.addEventListener('activate', (event) => {
    console.log('[sw] activate:', CACHE_NAME);

    event.waitUntil(
        caches.keys()
            .then((cacheNames) => Promise.all(
                cacheNames
                    .filter((name) => name !== CACHE_NAME)
                    .map((name) => {
                        console.log('[sw] удаляем устаревший кэш:', name);
                        return caches.delete(name);
                    })
            ))
            .then(() => self.clients.claim())
    );
});


/* ---------- 5. Событие 'fetch' — перехват сетевых запросов ---------- */
//
// СТРАТЕГИЯ: «Cache First, fallback to Network»
//   1. Сначала ищем запрашиваемый URL в кэше.
//   2. Если нашли — отдаём из кэша (мгновенно, работает офлайн).
//   3. Если не нашли — идём в сеть; параллельно кладём ответ в кэш,
//      чтобы в следующий раз отдать из кэша уже мгновенно.
//   4. Если и сеть недоступна — возвращаем index.html как «fallback»
//      для навигационных запросов (чтобы вкладка не упала с ошибкой).
//
// Альтернативные стратегии (полезно знать на экзамене):
//   - Network First: сначала сеть, кэш как запасной (хорошо для API).
//   - Stale-While-Revalidate: отдаём из кэша, в фоне обновляем.
//   - Network Only / Cache Only: соответственно, только сеть / только кэш.
//
// ВАЖНО: для нашей задачи не имеет смысла перехватывать запросы,
// которые НЕ являются GET (например, POST в API). Их пропускаем
// «как есть» — пусть браузер сам решает, что с ними делать.

self.addEventListener('fetch', (event) => {
    const request = event.request;

    // Не вмешиваемся в не-GET запросы (POST/PUT/DELETE и т.д.).
    if (request.method !== 'GET') return;

    event.respondWith(
        caches.match(request).then((cachedResponse) => {
            // Если ресурс лежит в кэше — отдаём его сразу.
            if (cachedResponse) {
                return cachedResponse;
            }

            // Иначе — идём в сеть.
            return fetch(request)
                .then((networkResponse) => {
                    // Успешные ответы кладём в кэш для будущих визитов.
                    // Условие проверяет, что ответ:
                    //   - пришёл (не undefined),
                    //   - имеет статус 200 (а не 404/500),
                    //   - типа 'basic' (то есть от того же origin).
                    // Для cross-origin ('cors', 'opaque') кэширование
                    // требует осторожности и чаще делается отдельно.
                    if (
                        networkResponse &&
                        networkResponse.status === 200 &&
                        networkResponse.type === 'basic'
                    ) {
                        // Response — это поток, его можно прочитать ОДИН раз.
                        // Чтобы и отдать в браузер, и положить в кэш —
                        // делаем clone(). Без этого вторая операция упадёт.
                        const responseClone = networkResponse.clone();
                        caches.open(CACHE_NAME).then((cache) => {
                            cache.put(request, responseClone);
                        });
                    }
                    return networkResponse;
                })
                .catch(() => {
                    // Сеть недоступна, в кэше тоже нет.
                    // Для навигационных запросов (открытие страницы)
                    // отдадим index.html — тогда хотя бы оболочка
                    // приложения покажется, и пользователь не увидит
                    // браузерную ошибку «No internet».
                    if (request.mode === 'navigate') {
                        return caches.match('./index.html');
                    }
                    // Для остальных запросов (картинки, шрифты и т.д.)
                    // вернём «честную» ошибку — пусть JS её обработает.
                    return new Response('Офлайн и ресурса нет в кэше', {
                        status: 503,
                        statusText: 'Service Unavailable',
                        headers: { 'Content-Type': 'text/plain; charset=utf-8' },
                    });
                });
        })
    );
});


/* ---------- 6. (Бонус для понимания) Событие 'message' ---------- */
//
// Не используется в текущем задании, но часто спрашивают на экзамене.
// Через postMessage страница может отправлять команды воркеру —
// например, попросить пропустить ожидание и сразу активироваться.
//
// Пример со страницы:
//     navigator.serviceWorker.controller.postMessage({ type: 'SKIP_WAITING' });
//
// Принимаем здесь:
self.addEventListener('message', (event) => {
    if (event.data && event.data.type === 'SKIP_WAITING') {
        self.skipWaiting();
    }
});
